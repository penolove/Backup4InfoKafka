TupleMappings* SELF::initMappings()
{
    instantiators_.insert(std::make_pair("tuple<rstring srcAddr,uint16 srcPort,rstring dstAddr,uint16 dstPort>",&initer));
    TupleMappings * tm = new TupleMappings();
#define MY_OFFSETOF(member, base) \
    ((uintptr_t)&reinterpret_cast<Self*>(base)->member) - (uintptr_t)base
   
    // initialize the mappings 
    
    {
        std::string s("srcAddr");
        TypeOffset t(MY_OFFSETOF(srcAddr_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 0);
    }
    
    {
        std::string s("srcPort");
        TypeOffset t(MY_OFFSETOF(srcPort_, tm), 
                     Meta::Type::typeOf<SPL::uint16 >(), 
                     &typeid(SPL::uint16));
        addMapping(*tm, t, s, 1);
    }
    
    {
        std::string s("dstAddr");
        TypeOffset t(MY_OFFSETOF(dstAddr_, tm), 
                     Meta::Type::typeOf<SPL::rstring >(), 
                     &typeid(SPL::rstring));
        addMapping(*tm, t, s, 2);
    }
    
    {
        std::string s("dstPort");
        TypeOffset t(MY_OFFSETOF(dstPort_, tm), 
                     Meta::Type::typeOf<SPL::uint16 >(), 
                     &typeid(SPL::uint16));
        addMapping(*tm, t, s, 3);
    }
    
    return tm;
}

